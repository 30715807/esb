package demo;

public class User
{
	String code = "1";
	String pwd = "2";
	String[] name = {"a", "b"};

	public String getCode()
	{
		return code;
	}

	public void setCode(String code)
	{
		this.code = code;
	}

	public String getPwd()
	{
		return pwd;
	}

	public void setPwd(String pwd)
	{
		this.pwd = pwd;
	}

	public String[] getName()
	{
		return name;
	}

	public void setName(String[] name)
	{
		this.name = name;
	}
}
